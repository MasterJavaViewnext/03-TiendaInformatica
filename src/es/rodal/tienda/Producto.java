package es.rodal.tienda;

public class Producto {
	private int codigo;
	private String modelo;
	private double precio;
	private int cantidad;
	
	public Producto(int codigo, String modelo, double precio, int cantidad) {
		super();
		this.codigo = codigo;
		this.modelo = modelo;
		this.precio = precio;
		this.cantidad = cantidad;
	}
	
}
